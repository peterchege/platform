                                            as a result of illness or accident.
                                        </p2>

                                        <hr>
                                        <h3>Critical Illness:</h3>
                                        <p2>If a member contracts a specified critical illness for the time
                                            (the first diagnosis). The specified illnesses are cancer, kidney failure,
                                            Paraplegia, heart attack for the first time in their life, Stroke, Coronary
                                            artery bypass surgery and major organ transplant. 30% of the accepted group
                                            life sum assured subject to an upper limit range of between Kshs. 1 to Kshs.
                                            12 million will be payable. On subsequent death 70% or 100% will be payable
                                            to the next of kin depending on whether the benefit is stand-alone or accelerated.
                                        </p2>
                                        <hr>
                                        <h3>Group Disability Income Insurance:</h3>
                                        <p2>This rider Compensates for lost income during the disability period. The employer
                                            chooses the maximum period for disability benefit payment either 2years,
                                            5years or up to normal retirement age.
                                        </p2>
                                        <hr>
                                        <h3>Temporary Total Disability:</h3>
                                        <p2>This benefit is payable if a member becomes wholly but temporarily incapacitated
                                            by bodily injury following his usual occupation hence unable to earn an income.
                                            The benefit payable is the actual weekly earnings for periods ranging between 1 to 2 years.
                                        </p2>
                                        <h3>Medical Reimbursement:</h3>
