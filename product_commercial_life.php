                                <h2>Group Life </h2>
                                <P class="text-justify">Group Life insurance is a benefit offered by an employer or
                                    an aggregator of diverse segments of the population for reasons other than insurance
                                    (like an association or labor organization) to its workers or members. Such
                                    organizations are called the sponsor of the group life scheme. Group life insurance
                                    is typically offered as part of employee / membership benefit package.</P>

                                <P class="text-justify">Ideally the cover will cost each individual worker or member much
                                    less than if they had to purchase an individual policy. Those receiving coverage do not
                                    have to pay anything “out of pocket” for the Group Life policy benefits if the employer
                                    or the group to which they belong provides this as a benefit. However, members of the
                                    group or employees may pay directly through salary or annual subscriptions if the employer
                                    or the group plays the role of a facilitator only. Usually a minimum number of employees
                                    or members are prescribed for the team to qualify as a group for the purpose of taking a
                                    group life cover.</P>
