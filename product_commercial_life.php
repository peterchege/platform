                                    <div class="text-justify">
                                        <h2>What is covered under the Group life policy have?</h2>
                                        <p> Death Benefits:</p>
                                        <p2>This benefit is payable on death from illness (natural causes) or accidental.
                                            The amount payable, called the sum assured, is either a fixed sum for all members
                                            in a particular category as predetermined by the sponsor or is determined as a
                                            multiple of the annual salary as set by the sponsor. The benefit is normally
                                            paid out as a lump sum to either the sponsor or the designated beneficiaries
                                            of the deceased employee / member.
                                        </p2>
                                        <p>Last expense: </p>
                                        <p2>This is normally taken as an additional benefit to help facilitate the disposal
                                            of the mortal remains of the deceased member. The benefit amount ranges between
                                            Kshs. 30,000 and Kshs. 200,000/- either as a separate cover or as acceleration
                                            (advance payment) of part of the full death benefit described above. Customarily,
                                            this benefit is paid within 48hours of receipt of written notification of the death.
                                        </p2>
                                        <p>Credit Life Assurance& Sacco loan protection: </p>
                                        <p2>Group life plans are also used to provide cover to redeem the outstanding loan balances
                                            in the event of premature death or disability of the borrower. This cover can be renewed
                                            every year (annually renewable) or provided at the onset of the loan agreement for the
                                            entire term of the loan (single premium option). All lenders with a minimum of 10
                                            borrowers including financial institutions and Sacco’s qualify for this cover.
                                        </p2>
                                        <p>Mortgage Protection: </p>
                                        <p2>Group life schemes offer mortgage protection arranged on a yearly renewable or single
                                            premium basis for lenders to specifically protect the families of the deceased mortgagor
                                            from the risk of the lender reselling their home in the event of the untimely death or
                                            disability of the breadwinner. This cover provides the same benefit as for credit life.
                                        </p2>
                                        <hr>
                                        <p> What are the age limits for the Group Life Assured plan?</p>
                                        <p2>The minimum age at entry is 18 years while the maximum age at entry is 65.
                                            Beyond the age of 70 years, renewal for the respective member is reviewed
                                            individually.</p2>
